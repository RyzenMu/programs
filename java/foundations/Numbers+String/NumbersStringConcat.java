public class NumbersStringConcat {

    public static void main(String[] args) {

        //Numbers + Numbers
        int x = 10;
        int y = 20;

        int z = x+y;
        System.out.println(z);


        // Numbers + String
        int a = 22;
        String b = "dog";

        String c = a + b ;
        String d = b + a ;
        System.out.println(c);
        System.out.println(d);


    }
}