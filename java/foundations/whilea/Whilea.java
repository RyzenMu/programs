

public class Whilea {

    public static void main(String[] args) {

        // General loop
        int s = 0;
        while (s <= 9) {
            System.out.println(s);
            s++;
        }


        // do while loop
        int d = 1;
        do {
            System.out.println(d);
            d++;
        }
        while(d <= 15);
    }
}