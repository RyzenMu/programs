public class Variables {

    public static void main(String[] args){
        
        // Declaring a string variable 
        String MyName = "Murugananatham";
        System.out.println(MyName);

        //Declaring a number variable
        int myNum = 5456;
        myNum = 65465;
        System.out.println(myNum);

        // final == constant
        final int num1 = 54;
        // num1 = 56; --- now value cannot be assigned to a final int 
        System.out.println(num1);

        //float assignment
        float num2 = 2.66f;
        System.out.println(num2);

        //char assignment within single quotes
        char char1 = 's';
        System.out.println(char1);

        //boolean assignment
        boolean myBool = true;
        System.out.println(myBool);

        // assign volvo to a variable
        String myCar = "Volvo";
        System.out.println(myCar);




    }
}
