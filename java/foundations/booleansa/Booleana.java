

public class Booleana {

    public static void main(String[] args) {

        // initializing a booleannnn
        boolean a = true;
        System.out.println("condition of a is :" +  a);
        System.out.println("condition of !a is :" +  !a);



        // boolean expression
        System.out.println(57 > 91);
        System.out.println(57 < 91);
        System.out.println(57 == 91);


        // example of boolean with if else case

        int myAge  = 15;
        int votingAge  = 20;

        if (myAge >= votingAge){
            System.out.println("You can vote from this election");
        } else {
            System.out.println("You have to wait");
        }

        


    }
    
}
